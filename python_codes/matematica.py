def soma(a: int, b: int) -> int:
    """Uma simples função de soma
    
    Args:
        a (int): Um inteiro de sua preferência
        b (int): Um inteiro de sua preferência
    
    Examples:
        >>
        >> soma(2,3)
        5
    
    Returns:
        int: a soma de dois inteiros

    """
    return a + b

def subtrai(a: int, b: int) -> int:
    """Uma simples funcao de subtracao
    
    Args:
        a (int): Um inteiro de sua preferência
        b (int): Um inteiro de sua preferência
    
    Returns:
        int: a subtracao de a por b

    """
    return a - b 